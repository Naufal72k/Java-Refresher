package Kalkulator;

public class Kalkulator {
    void tambah(int a, int b) {
        int hasil = a + b;
        System.out.println("Tambah :" + hasil);
    }

    void kurang(int a, int b) {
        int hasil = a - b;
        System.out.println("Kurang :" + hasil);
    }

    void kali(int a, int b) {
        int hasil = a * b;
        System.out.println("Kali :" + hasil);
    }

}
